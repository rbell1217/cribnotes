#!/usr/bin/env node
/**
 * Vercel build step: generate js/config.js from environment variables.
 *
 * On Vercel, set these env vars under Project Settings > Environment Variables:
 *   FIREBASE_API_KEY
 *   FIREBASE_AUTH_DOMAIN
 *   FIREBASE_PROJECT_ID
 *   FIREBASE_STORAGE_BUCKET
 *   FIREBASE_MESSAGING_SENDER_ID
 *   FIREBASE_APP_ID
 *   FIREBASE_MEASUREMENT_ID    (optional)
 *
 * Locally this script is not needed -- you hand-edit js/config.js with your
 * Firebase credentials and that file is gitignored. The build step takes over
 * for production deploys.
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const OUT = path.resolve(__dirname, '..', 'js', 'config.js');

const required = [
  'FIREBASE_API_KEY',
  'FIREBASE_AUTH_DOMAIN',
  'FIREBASE_PROJECT_ID',
  'FIREBASE_STORAGE_BUCKET',
  'FIREBASE_MESSAGING_SENDER_ID',
  'FIREBASE_APP_ID'
];

const missing = required.filter(key => !process.env[key] || process.env[key].trim() === '');
if (missing.length) {
  console.error('[CribNotes build] Missing required env vars:', missing.join(', '));
  console.error('[CribNotes build] Set them in Vercel > Project Settings > Environment Variables.');
  process.exit(1);
}

const escape = s => String(s).replace(/[\\"]/g, '\\$&');

const measurementLine = process.env.FIREBASE_MEASUREMENT_ID
  ? `,\n  measurementId: "${escape(process.env.FIREBASE_MEASUREMENT_ID)}"`
  : '';

const out = `/**
 * Firebase Configuration -- generated by build/inject-config.js at deploy time.
 * Do not edit by hand on the deployed copy. Edit env vars in Vercel instead.
 * For local development, this file is gitignored; populate it manually.
 */

const firebaseConfig = {
  apiKey: "${escape(process.env.FIREBASE_API_KEY)}",
  authDomain: "${escape(process.env.FIREBASE_AUTH_DOMAIN)}",
  projectId: "${escape(process.env.FIREBASE_PROJECT_ID)}",
  storageBucket: "${escape(process.env.FIREBASE_STORAGE_BUCKET)}",
  messagingSenderId: "${escape(process.env.FIREBASE_MESSAGING_SENDER_ID)}",
  appId: "${escape(process.env.FIREBASE_APP_ID)}"${measurementLine}
};

export function isFirebaseConfigured() {
  return firebaseConfig.apiKey !== "YOUR_API_KEY" && firebaseConfig.projectId !== "YOUR_PROJECT_ID";
}

export default firebaseConfig;
`;

fs.writeFileSync(OUT, out, 'utf8');
console.log('[CribNotes build] Wrote', OUT);
